const reliquaryCooldown = {}

function playSound(player, soundId, volume, pitch) {
  player.runCommandSilent('playsound ' + soundId + ' player @s ~ ~ ~ ' + volume + ' ' + pitch)
}

function pickWeighted(pool) {
  var total = 0
  for (var i = 0; i < pool.length; i++) {
    total += pool[i].weight
  }
  var roll = Math.random() * total
  for (var j = 0; j < pool.length; j++) {
    roll -= pool[j].weight
    if (roll <= 0) {
      return pool[j]
    }
  }
  return pool[pool.length - 1]
}

function rollCount(entry) {
  return entry.min + Math.floor(Math.random() * (entry.max - entry.min + 1))
}

function giveReliquaryLoot(player, lootId) {
  var pool = RELIQUARY_POOLS[lootId]
  if (!pool) {
    player.tell('§cРеликвария пуста — сообщите диспетчеру.')
    return false
  }
  var rolls = 2 + Math.floor(Math.random() * 3)
  var given = 0
  for (var i = 0; i < rolls; i++) {
    var entry = pickWeighted(pool)
    var count = rollCount(entry)
    player.give(Item.of(entry.item, count))
    given++
  }
  return given > 0
}

global.cooptechOpenJournal = function (entity) {
  if (!entity) return

  var stack = null
  if (entity.getMainHandItem) {
    stack = entity.getMainHandItem()
  } else if (entity.mainHandItem) {
    stack = entity.mainHandItem
  }
  if (stack && stack.id !== 'kubejs:expedition_journal') {
    return
  }

  if (global.cooptechSyncJournalUnlocks) {
    global.cooptechSyncJournalUnlocks(entity)
  }

  if (entity.sendData) {
    entity.sendData('cooptech_open_journal', { open: true })
  }
}

global.cooptechReadJournalPage = function (player, pageId) {
  var lines = PAGE_LINES[pageId]
  if (!lines) {
    player.tell('§7Страница §8' + pageId.replace('journal_page_', '#') + '§7 — пустой лист, ждёт ваших записей.')
    return
  }
  lines.forEach(function (line) { player.tell(line) })
  playSound(player, 'minecraft:entity.item.pickup', 0.35, 1.2)

  if (pageId === 'journal_page_00') {
    try {
      player.runCommandSilent('advancement grant ' + player.username + ' only cooptech:journal/page_00_read')
    } catch (e) { /* optional */ }

    try {
      var hasRubeg = false
      var adv = player.advancements
      if (adv && adv.has) {
        hasRubeg = adv.has('cooptech:journal/rubeg_2')
      }
      if (hasRubeg) {
        player.runCommandSilent('advancement grant ' + player.username + ' only cooptech:journal/dispatcher_briefing')
        player.tell('§5§l[Журнал «Якорь»]§r §fПриказ диспетчера зафиксирован в кодексе.')
      }
    } catch (e2) { /* optional */ }
  }
}

global.cooptechOpenReliquary = function (player, lootId) {
  var key = String(player.uuid)
  var now = Date.now()
  if (reliquaryCooldown[key] && now - reliquaryCooldown[key] < 3000) {
    player.tell('§eПодождите перед следующим вскрытием реликварии.')
    return
  }
  reliquaryCooldown[key] = now
  playSound(player, 'minecraft:block.amethyst_block.chime', 1.0, 1.0)
  if (giveReliquaryLoot(player, lootId)) {
    player.tell('§dРеликвария вскрыта. Проверьте инвентарь.')
  }
}

global.cooptechUseBeacon = function (player) {
  var COOP_TASK_ID = 'T101000000000015'
  var SPAWN_RADIUS = 48
  var CHECKIN_KEY = 'cooptech_prologue_wave2'

  var spawn = player.level.getSharedSpawnPos()
  var dx = player.x - spawn.x
  var dz = player.z - spawn.z
  if (Math.sqrt(dx * dx + dz * dz) > SPAWN_RADIUS) {
    player.tell('§7[Маяк] §cПодойдите к лагерю экспедиции (рядом со спавном).')
    return
  }

  var data = player.level.persistentData
  if (!data.contains(CHECKIN_KEY)) {
    data.putString(CHECKIN_KEY, '[]')
  }
  var list
  try {
    list = JSON.parse(data.getString(CHECKIN_KEY))
  } catch (e) {
    list = []
  }

  var id = String(player.uuid)
  if (list.indexOf(id) >= 0) {
    player.tell('§7[Маяк] §fВы уже отметились. Ждём напарника.')
    return
  }
  list.push(id)
  data.putString(CHECKIN_KEY, JSON.stringify(list))
  player.tell('§7[Маяк] §aСигнал отправлен. §f(' + list.length + '/2)')

  if (list.length >= 2) {
    player.server.runCommandSilent('ftbquests change_progress ' + player.username + ' complete ' + COOP_TASK_ID)
    player.level.players.forEach(function (p) {
      p.tell('§d[Якорь] §fСинхронизация команды подтверждена.')
    })
    data.putString(CHECKIN_KEY, '[]')
  }
}
