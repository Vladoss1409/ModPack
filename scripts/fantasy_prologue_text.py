def main() -> int:
    text = SNBT.read_text(encoding='utf-8')
    original = text
    for old, new in REPLACEMENTS:
        if old not in text:
            print(f'  skip (not found): {old[:50]}...')
        else:
            text = text.replace(old, new)
            print(f'  ok: {old[:40]}...')
    if text == original:
        print('No changes made.')
        return 0
    SNBT.write_text(text, encoding='utf-8')
    print(f'Updated {SNBT.name}')
    return 0


