# filename -> advancement key (cooptech:journal/…)
SIDE_ADV_KEYS: dict[str, str] = {fn: fn for fn in SIDE_BRANCH_LORE}
SIDE_ADV_KEYS["side_silent_gear"] = "side_silent_gear"


def get_side_quest_content(filename: str, num: int, fallback_title: str) -> tuple[str, list[str], str]:
    branch = SIDE_BRANCH_LORE.get(filename)
    if not branch:
        return fallback_title, lore(f"Задача ветки: {fallback_title}.", "Сайд-ветка — свой ритм."), f"Выполнить: {fallback_title}"
    entry = branch.get("quests", {}).get(num)
    if entry:
        return entry
    return fallback_title, lore(f"Следующий шаг ветки — {fallback_title}.", "Записываю по ходу."), f"Выполнить: {fallback_title}"


def side_intro(filename: str) -> list[str]:
    branch = SIDE_BRANCH_LORE.get(filename)
    return branch.get("intro", []) if branch else []


def side_patchouli_text(filename: str) -> str:
    branch = SIDE_BRANCH_LORE.get(filename)
    return branch.get("patchouli", "") if branch else ""
